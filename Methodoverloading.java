class area
{
    void area(float x)
    {
        System.out.println("Area of circle is:"+3.14*x*x);
    }
    void area(float x,float y)
    {
        System.out.println("Area of rectangle is:"+x*y);
    }
    void area(int x,int y)
    {
        System.out.println("Area of triangle is:"+0.5*x*y);
    }
    
}